package com.ehrmann.viatcheslav.othpost.entity;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;

/**
 *
 * @author Viatcheslav Ehrmann
 */
@Entity
public class Postoffice extends LongIdEntity {
    public Warehouse getContractWarehouse() {
        return contractWarehouse;
    }

    public void setContractWarehouse(Warehouse contractWarehouse) {
        this.contractWarehouse = contractWarehouse;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getStreetNumber() {
        return streetNumber;
    }

    public void setStreetNumber(String streetNumber) {
        this.streetNumber = streetNumber;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getPostalcode() {
        return postalcode;
    }

    public void setPostalcode(int postalcode) {
        this.postalcode = postalcode;
    }
    
    @ManyToOne
    private Warehouse contractWarehouse;
    private String street;
    private String streetNumber;
    private String city;
    private int postalcode;
    
}
